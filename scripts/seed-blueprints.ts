async function main() {
  const col = db.collection("blueprints");
  const batch = db.batch();

  for (const bp of blueprints) {
    const ref = col.doc(bp.templateID);
    batch.set(ref, bp);
  }

  await batch.commit();
  console.log(`✅ ${blueprints.length} 件を blueprints コレクションに書き込みました。`);
  process.exit(0);
}

main().catch((e) => { console.error(e); process.exit(1); });
